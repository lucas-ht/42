#include "libft.h"

double	ft_vector3_dist(t_vector3 v, t_vector3 w, double (*sqrt)(double))
{
	return (sqrt(
			((w.x - v.x) * (w.x - v.x))
			+ ((w.y - v.y) * (w.y - v.y))
			+ ((w.z - v.z) * (w.z - v.z))
		));
}
