#include "libft.h"

t_vector3	ft_euler_normalize(t_vector3 v)
{
	while (v.x > 360)
		v.x -= 360;
	while (v.x < 0)
		v.x += 360;
	while (v.y > 360)
		v.y -= 360;
	while (v.y < 0)
		v.y += 360;
	while (v.z > 360)
		v.z -= 360;
	while (v.z < 0)
		v.z += 360;
	return (v);
}
